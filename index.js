        let count = 92
        const myBtn = document.getElementById("myBtn");
        myBtn.addEventListener("click", function(){
          if(count >= 100){count = 100}
        else {count++}
            sBOverall.textContent= `${count} Overall`
        })
        const myBtnMinus = document.getElementById("myBtnMinus");
        myBtnMinus.addEventListener("click", function(){
            count --
            sBOverall.textContent= `${count} Overall`
        })


